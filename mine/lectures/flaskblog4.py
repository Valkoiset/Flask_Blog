# create database instance
db = SQLAlchemy(app)


class User(db.Model):
    id = db.Column(db.Integer, primary_key=True)
    username = db.Column(db.String(20), unique=True, nullable=False)
    email = db.Column(db.String(120), unique=True, nullable=False)
    image_file = db.Column(db.String(20), nullable=False,
                           default='default.jpg')
    password = db.Column(db.String(60), nullable=False)
    # here we're saying that our "posts" attribute has a relationship with the "Post model"
    # backref='author' gives the user who made a POST
    # lazy=True defines that SQLAlchemy loads the data as necessary in 1 GO
    # with this relationship we'll be able to simply use this "posts" attribute to get all of
    # the posts created by an individual user
    # relationship is not a column!
    posts = db.relationship('Post', backref='author', lazy=True)

    # so-called "dunder" or "magic" method responsible for how the object is printed
    def __repr__(self):
        return f"User('{self.username}', '{self.email}', '{self.image_file}')"


class Post(db.Model):
    id = db.Column(db.Integer, primary_key=True)
    title = db.Column(db.String(100), nullable=False)
    date_posted = db.Column(db.DateTime, nullable=False,
                            # here utcnow is without () because we want to put a function as default, not the current utc time
                            default=datetime.utcnow)
    content = db.Column(db.Text, nullable=False)
    user_id = db.Column(db.Integer, db.ForeignKey('user.id'), nullable=False)

    def __repr__(self):
        return f"Post('{self.title}', '{self.date_posted}')"


